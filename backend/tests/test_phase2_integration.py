def load_synthetic_data():
    """Load synthetic data generated by the data generator."""
    data_dir = 'synthetic_data'

    if not os.path.exists(data_dir):
        print("âŒ Synthetic data not found. Please run the data generator first.")
        return None

    try:
        with open(f'{data_dir}/resources.json', 'r') as f:
            resources = json.load(f)

        with open(f'{data_dir}/cost_entries.json', 'r') as f:
            cost_entries = json.load(f)

        with open(f'{data_dir}/usage_patterns.json', 'r') as f:
            usage_patterns = json.load(f)

        return {
            'resources': resources,
            'cost_entries': cost_entries,
            'usage_patterns': usage_patterns
        }

    except FileNotFoundError as e:
        print(f"âŒ Error loading synthetic data: {e}")
        return None

def test_ml_pipeline_integration():
    """Test the complete ML pipeline with synthetic data."""
    print("ğŸš€ Starting Phase 2 ML Pipeline Integration Test")
    print("=" * 60)

    # Load synthetic data
    data = load_synthetic_data()
    if not data:
        return False

    resources = data['resources']
    cost_entries = data['cost_entries']
    usage_patterns = data['usage_patterns']

    print(f"ğŸ“Š Testing with {len(resources)} resources, {len(cost_entries)} cost entries")

    # Initialize ML components
    analyzer = UsagePatternAnalyzer()
    assessor = RiskAssessor()
    recommender = OptimizationRecommender()
    predictor = PerformancePredictor()

    success_count = 0
    total_tests = 0

    # Test each resource
    for i, resource in enumerate(resources[:10]):  # Test first 10 resources
        resource_id = resource['id']
        print(f"\nğŸ” Testing Resource {i+1}/10: {resource_id} ({resource['resource_type']})")

        total_tests += 1

        try:
            # 1. Get usage data for this resource
            usage_data = usage_patterns.get(str(resource_id), [])
            if not usage_data:
                print(f"   âš ï¸  No usage data for resource {resource_id}")
                continue

            # 2. Analyze usage patterns
            print("   ğŸ“ˆ Analyzing usage patterns...")
            training_result = analyzer.train_model(usage_data)
            if training_result['status'] not in ['success', 'trained']:
                print(f"   âš ï¸  Usage analysis failed: {training_result['status']}")
                continue

            # 3. Generate recommendations
            print("   ğŸ’¡ Generating optimization recommendations...")
            resource_costs = [c for c in cost_entries if c.get('resource_id') == resource_id]
            recommendations = recommender.generate_recommendations(
                [resource],
                {resource_id: training_result.get('usage_patterns', {})},
                resource_costs
            )

            if not recommendations:
                print("   âš ï¸  No recommendations generated")
                continue

            # 4. Assess risk for top recommendation
            top_rec = recommendations[0]
            print(f"   âš ï¸  Assessing risk for: {top_rec['title']}")
            risk_assessment = assessor.assess_risk(resource, top_rec)

            # 5. Predict performance impact
            print("   ğŸ”® Predicting performance impact...")
            performance_prediction = predictor.predict_impact(
                resource, top_rec, training_result.get('usage_patterns', {})
            )

            # Validate results
            required_fields = ['overall_risk_score', 'risk_level']
            if all(field in risk_assessment for field in required_fields):
                print("   âœ… Risk assessment successful")
                print(".2f")
                print(f"   ğŸ“Š Risk Level: {risk_assessment['risk_level']}")
            else:
                print("   âŒ Risk assessment missing required fields")
                continue

            required_perf_fields = ['predicted_performance_impact', 'confidence_level']
            if all(field in performance_prediction for field in required_perf_fields):
                print("   âœ… Performance prediction successful")
                print(".2f")
                print(f"   ğŸ“Š Confidence: {performance_prediction['confidence_level']}")
            else:
                print("   âŒ Performance prediction missing required fields")
                continue

            # Calculate potential savings
            total_savings = sum(r.get('potential_savings', 0) for r in recommendations)
            print(".2f")
            success_count += 1
            print("   ğŸ‰ Resource test completed successfully")
        except Exception as e:
            print(f"   âŒ Error testing resource {resource_id}: {e}")
            continue

    # Calculate success rate
    success_rate = (success_count / total_tests) * 100 if total_tests > 0 else 0

    print("\n" + "=" * 60)
    print("ğŸ“‹ Phase 2 Integration Test Results")
    print("=" * 60)
    print(f"âœ… Successful Tests: {success_count}/{total_tests}")
    print(".1f")
    if success_rate >= 80:
        print("ğŸ‰ PHASE 2 INTEGRATION TEST PASSED")
        print("âœ… All core ML components working correctly")
        print("âœ… Risk assessment and performance prediction functional")
        print("âœ… Optimization recommendations generated successfully")
        return True
    else:
        print("âŒ PHASE 2 INTEGRATION TEST FAILED")
        print("âš ï¸  ML pipeline requires additional tuning")
        return False

def test_phase2_kpis():
    """Test Phase 2 KPIs against synthetic data."""
    print("\nğŸ¯ Testing Phase 2 KPIs")
    print("-" * 40)

    data = load_synthetic_data()
    if not data:
        return False

    resources = data['resources']
